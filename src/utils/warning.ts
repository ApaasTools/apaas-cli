export function outputWarning() {
  // 与旧版一致的提示文案（不阻断执行）
  process.stdout.write(
    [
      '--------------------------------- WARNING ----------------------------------------',
      '注意：此脚手架将逐步替代旧版 x-apaas-cli，如需旧文档请参考得帆历史文档。',
      '请使用 apaas 命令替代旧版 x-apaas-cli 命令。',
      '----------------------------------------------------------------------------------',
    ].join('\n') +
      '\n'
  );
}

