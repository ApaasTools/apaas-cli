export function outputWarning() {
  // 与旧版一致的提示文案（不阻断执行）
  process.stdout.write(
    [
      '--------------------------------- WARNING ----------------------------------------',
      '注意：此脚手架将逐步替代旧版 x-apaas-cli-old，如需旧文档请参考内部历史文档。',
      '----------------------------------------------------------------------------------',
    ].join('\n') +
      '\n'
  );
}

